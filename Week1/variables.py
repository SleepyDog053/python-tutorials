# There are different types of variables

# numbers (integers)
myInt = 7
print(myInt)
print(type(myInt))

# floats (decimal point numbers)

myFloat = 7.0
print(type(myFloat))
print(myFloat)
myFloat = float(7)
print(type(myFloat))
print(myFloat)

# strings
myString = "Hello"
print("Hello")

# You can add integers together

one = 1
two = 2
three = one + two
print(three)

# Booleans (True or False)

